salary = input("Enter your salary expectation: ")
print("The data type of ", salary, "is", type(salary))
# Converting into float data type
salary = float(salary)
print("The data type of", salary, "is", type(salary))
# Conveting into integer data type
salary = int(salary)
print("The data type of", salary, "is", type(salary))
# Converting back into string
salary = str(salary)
print("The data type of", salary, "is", type(salary))
