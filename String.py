# Initializing the String
a = "Good Morning"  # Also works with 'Good Morning' single quotes
print("String a:", a)
# -> "Good Morning"
b = "What's your plans today?"
print("String b:", b)
# -> "What's your plans today?"

print("Combining both strings using ',':\n", a, b)
# -> "Good Morning Whats your plans today?"

print("Combining both strings using '+':\n", a + b)
# -> "Good MorningWhat's your plans today"
